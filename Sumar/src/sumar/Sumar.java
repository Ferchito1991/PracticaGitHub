package sumar;

public class Sumar {

    public static void main(String[] args) {
        int NumeroA = 0;
        int NumeroB = 0;       
        sumar(NumeroA,NumeroB);
    }
    
    private static int sumar (int a, int b){
        int resultado = 0;
        resultado = a+b;
        return resultado;
    }
    
}
